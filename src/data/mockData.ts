const genFinancials = (
  base: number,
  growth: number,
  debtRatio: number
) => {
  return [2021, 2022, 2023].map((year, i) => {
    const multiplier = Math.pow(1 + growth, i);
    const revenue = base * multiplier;
    const netIncome = revenue * (0.08 + Math.random() * 0.1);
    const totalAssets = revenue * 2.4;
    const totalLiabilities = totalAssets * debtRatio;
    const equity = totalAssets - totalLiabilities;
    const currentAssets = totalAssets * 0.35;
    const currentLiabilities = totalLiabilities * 0.4;
    return {
      year,
      revenue: Math.round(revenue),
      netIncome: Math.round(netIncome),
      totalAssets: Math.round(totalAssets),
      totalLiabilities: Math.round(totalLiabilities),
      equity: Math.round(equity),
      operatingCashFlow: Math.round(netIncome * 1.3),
      ebitda: Math.round(revenue * 0.2),
      grossProfit: Math.round(revenue * 0.35),
      currentAssets: Math.round(currentAssets),
      currentLiabilities: Math.round(currentLiabilities),
      retainedEarnings: Math.round(equity * 0.45),
      intangibleAssets: Math.round(totalAssets * 0.05),
      workingCapital: Math.round(currentAssets - currentLiabilities),
      marketCap: isNaN(equity * 1.8) ? undefined : Math.round(equity * 1.8),
    };
  });
};

const calcAltmanZ = (fin: ReturnType<typeof genFinancials>[0]) => {
  const x1 = fin.workingCapital / fin.totalAssets;
  const x2 = fin.retainedEarnings / fin.totalAssets;
  const x3 = fin.ebitda / fin.totalAssets;
  const x4 = (fin.marketCap ?? fin.equity * 1.5) / fin.totalLiabilities;
  const x5 = fin.revenue / fin.totalAssets;
  const z = 1.2 * x1 + 1.4 * x2 + 3.3 * x3 + 0.6 * x4 + 1.0 * x5;
  return {
    x1, x2, x3, x4, x5,
    zScore: parseFloat(z.toFixed(2)),
    bankruptcyRisk: z > 2.99 ? 'safe' as const : z > 1.81 ? 'grey' as const : 'distress' as const,
    bankruptcyProbability: z > 2.99 ? Math.round(5 + Math.random() * 10) : z > 1.81 ? Math.round(20 + Math.random() * 20) : Math.round(45 + Math.random() * 30),
  };
};

